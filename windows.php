                                <h2 style="color: #F067FF">WINDOWS 11: "REINVENTANDO" EL SISTEMA.
                                </h2>
                                <ul class="blog-info-link mt-3 mb-4">
                                    <li><a href="#"><i class="fa fa-user"></i> Tecnología</a></li>
                                    <li><a href="#"><i class="fa fa-comments"></i> 00 Comments</a></li>
                                </ul>
                                <p class="excert">
                                    <strong>Windows</strong> es el sistema operativo más usado en el mercado en lo que a computadoras se refiere y, a lo largo de los años, ha sufrido diferentes cambios dependiendo de la versión, unos para bien y otros no tanto. Pero, a pesar de los cambios, el ambiente de Windows siempre ha sido el mismo (Con las excepciones de <strong>Windows Vista</strong> y <strong>Windows 8</strong>).
                                </p>
                                <p>
                                    Ahora Microsoft buscar darle una nueva apariencia más moderna a su software principal, con el fin de generar un ambiente limpio para el usuario dejando de lado la extraña experiencia de Windows 10 compuesta de todas sus actualizaciones.
                                </p>
                                <div class="quote-wrapper" style="display:none;">
                                    <div class="quotes">
                                        MCSE boot camps have its supporters and its detractors. Some people do not understand why you
                                        should have to spend money on boot camp when you can get the MCSE study materials yourself at
                                        a fraction of the camp price. However, who has the willpower to actually sit through a
                                        self-imposed MCSE training.
                                    </div>
                                </div>
                                <p>
                                    <strong>Panos Panay</strong>, ex-director de Surface, desea que Windows vuelva a estar en la cima de los sistemas operativos, dándole un aspecto novedoso a la interfaz del software implementando cambios substanciales en apartados que llevaban años sin una actualización.
                                </p>

                                <center><strong style="font-size: large; color:#2D78CE;">Un cambio de ideología </strong></center>
                                <br>

                                <center><img class="img-fluid" src="https://www.solucionesrp.com.mx/assets/img/resources/idea.jpg" alt=""></center>
                                <br>

                                <p>
                                    Durante mucho tiempo, por no decir siempre, el sistema de Microsoft estaba dirigido principalmente a las empresas, ofreciéndoles características que iban acorde a este tipo de ambientes pero que sin problema eran usados en equipos hogare&ntilde;os y escolares. Sin embargo, se busca erradicar esta idea con Windows 11, el cual estará enfocado principalmente en el usuario promedio. Buscan que deseen usar el sistema y, primordialmente, que disfruten trabajar con él.
                                </p>
                                <p>
                                    Por lo que Windows 11 será enfocado para actividades cotidianas, mientras que 10 se mantendrá como el software empresarial por excelencia.
                                </p>

                                <center><strong style="font-size: large; color:#2D78CE;">Novedades por venir</strong></center>

                                <br>

                                <center><img class="img-fluid" src="https://www.solucionesrp.com.mx/assets/img/resources/w2.png" alt=""></center>
                                <center><small><strong>El antes y después de los iconos de windows.</strong></small></center>
                                <br>

                                <p>El cambio más notorio que tendremos en el nuevo sistema, será la interfaz. Esta implementará una nueva iconografía, fresca y novedosa, que permita resaltar los cambios en la distribución de los elementos en los menús. Buscando una armonía en todos los rincones del sistema. (No como en Windows 10, donde hay una combinación extraña de ellos).</p>


                                <p>Otros protagonistas en este cambio de imagen son el menú de inicio y la barra de tareas, los culés también estrenan características. </p>

                                <br>

                                <center><img class="img-fluid" src="https://www.solucionesrp.com.mx/assets/img/resources/w1.jpg" alt=""></center>
                                <center><small><strong>Vista preliminar de la interfaz de Windows 11.</strong></small></center>
                                <br>

                                <p>Como se puede observar, el menú de inicio ahora se encuentra en la parte central de la pantalla, dándole un poco más de protagonismo y presencia. Al desplegarlo, se muestra una venta en donde podemos apreciar un listado de aplicaciones, que esperemos se puedan modificar, y en la esquina superior derecha el botón para nostrarlas todas. </p>


                                <p>De igual forma, debajo de las aplicaciones, se encuentran archivos recientes con los que hemos interactuado. Además de accesos directos a ciertas opciones como nuestro perfil, documentos, imágenes, configuraciones y las ya clásicas funciones de apagado. </p>



                                <p>

                                    Con respecto a la barra de tareas, esta cuenta con un diseño minimalista en donde los iconos son más pequeños, pero resaltan por la simpleza del menú. Esta es completamente personalizable, o por lo menos eso se aprecia en las imágenes, podemos ver ejemplos donde hay pocos iconos y otros donde contamos con más de ellos.

                                </p>

                                <center><img class="img-fluid" src="https://www.solucionesrp.com.mx/assets/img/resources/w3.jpg" alt=""></center>
                                <center><small><strong>Windows 11 con la distribución clásica y el centro de actividades.</strong></small> </center>
                                <br>

                                <p>Otro cambio a resaltar es el nuevo centro de actividades. Aquí tendremos acceso a las ya clásicas notificaciones, un control de música, las opciones de sonido y varios accesos a funciones del sistema. </p>

                                <center><strong style="font-size: large; color:#2D78CE;">Las aplicaciones nativas también estrenan mejoras</strong></center>

                                <br>

                                <p>Microsoft también se ha propuesto el darle un nuevo aspecto a las aplicaciones que vienen integradas con el sistema, por lo que estrenaremos aplicaciones como Fotos, Calendario, Música y configuración. De esta última parece que ya se ha filtrado su rediseño. </p>

                                <p>Donde ya tenemos la nueva interfaz disponible para revisar, es en la app de Alarmas y Relog, la cual fue implementado hace unas semanas. </p>

                                <center><img class="img-fluid" src="https://www.solucionesrp.com.mx/assets/img/resources/alarma.jpeg" alt=""></center>
                                <center><small><strong>Nueva interfaz de la aplicación de Alarmas y Relog.</strong></small> </center>

                                <br>

                                <p>Dentro del sistema Windows, a partir de la versión 8, se implementó la aplicación de <strong>Microsoft Store</strong>, en donde se pueden obtener aplicaciones solamente de algunas fuentes, manteniendo la instalación de algunos programas de forma externa los cuales se pueden descargar desde su portal web. </p>

                                <p>Esto cambiaria con la implementación de la nueva tienda, ya que dará la enviar a la Tienda aplicaciones Win32 no empaquetadas (tanto en formato .exe como .MSI), alojar apps y actualizaciones en su propio CDN y utilizar plataformas comerciales de terceros en sus aplicaciones.</p>

                                <p>Además, la comisión impuesta por la tienda se reducirá de un <strong>30%</strong> a un <strong>12%</strong>, en lo relacionado a los videojuegos. Esto impone presión sobre otras plataformas que manejan la misma tarifa. Microsoft busca ser la mejor opción en lo que a servicios de distribución de aplicaciones se refiere, lo cual orilla a los demás portales a mantener una competitividad, de esta manera los usuarios podremos obtener mayores beneficios. </p>

                                <center><strong style="font-size: large; color:#2D78CE;">Un cambio para bien</strong></center>

                                <br>

                                <p>La empresa de Bill Gates está implementando una restructuración que parece, de momento, solamente traer mejoras. Con todos los cambios en el sistema, nuevas funciones que se agregarán a Windows, además de la modificación en las políticas de la tienda, Microsoft está demostrando una evolución en su modelo de negocios que es beneficioso para todos. </p>

                                <p>Por ahora solo nos queda esperar para experimentar y juzgar las modificaciones que se estarán implementando. La fecha de presentación del sistema está programada para el próximo <strong>24 de junio</strong>, por lo que tendremos que estar atentos para saber toda la información del software estrella de Microsoft. </p>

